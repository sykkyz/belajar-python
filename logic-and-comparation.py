# PROGRAM GACHA ANGKA PERTAMA

print("============= GACHA 1 =============\n")
print("Program ini berguna untuk mengecek\n   apakah angka yang dimasukkan\n berada diantara 0 - 5 dan 8 - 11")

angka = float(input("\nMasukkan angka: "))

# mengecek apakah angka tersebut adalah x dan 0 < x < 5
# lebihNol = (angka > 0)
# kurangLima = (angka < 5)

# nolLima = lebihNol and kurangLima
nolLima = (angka > 0) and (angka < 5)
print("\nAngka yang telah dimasukkan berada antara 0 sampai 5:", nolLima)

# mengecek apakah angka tersebut adalah x dan 8 < x < 11
# lebihDelapan = (angka > 8)
# kurangSebelas = (angka < 11)

# delapanSebelas = lebihDelapan and kurangSebelas
delapanSebelas = (angka > 8) and (angka < 11)
print("Angka yang telah dimasukkan berada antara 8 sampai 11:", delapanSebelas)

hasilAkhir = nolLima or delapanSebelas
print("Angka yang dimasukkan:",hasilAkhir,"sesuai dengan fungsi program ini\n")

